		--BUSCAR POR ID
		CREATE PROCEDURE BUSCAR_VEHICULO
		@IDVEHICULO INT
		AS
		SELECT V.IDVEHICULO AS ID,V.DESCRIPCION AS ESTADO,TV.NOMBRE AS TIPO_VEHICULO,M.NOMBRE AS MARCA, MO.NOMBRE AS MODELO
		FROM VEHICULO V INNER JOIN TIPO_VEHICULO TV
		ON V.IDTIPOVEHICULO = TV.IDTIPOVEHICULO
		INNER JOIN MARCA M 
		ON V.IDMARCA = M.IDMARCA
		INNER JOIN MODELO MO
		ON V.IDMODELO = MO.IDMODELO
		WHERE V.IDVEHICULO = @IDVEHICULO

		--BUSCAR POR VEHICULO MARCA
		CREATE PROCEDURE BUSCAR_VEHICULO_MARCA
		@IDMARCA INT
		AS
		SELECT V.IDVEHICULO AS ID, V.DESCRIPCION AS ESTADO, TV.NOMBRE AS TIPO_VEHICULO, M.NOMBRE AS MARCA, MO.NOMBRE AS MODELO
		FROM VEHICULO V INNER JOIN TIPO_VEHICULO TV
		ON V.IDTIPOVEHICULO = TV.IDTIPOVEHICULO
		INNER JOIN MARCA M
		ON V.IDMARCA = M.IDMARCA
		INNER JOIN MODELO MO
		ON V.IDMODELO = MO.IDMODELO
		WHERE M.IDMARCA = @IDMARCA

		--CARGAR COMBO MARCA
		CREATE PROCEDURE CARGAR_COMBO_MARCA
		AS
		SELECT IDMARCA AS ID, NOMBRE AS DESCRIPCION FROM MARCA

